<h3 class="text-muted"> 404 not found page example</h3>
<p>
    An example of <i> not found </i> page. To change this go to <code>Application\Settings\Config.php </code>
    and then change in
    <code>
        <br>
        'notFound' => [ <br>
        'controller' => '\Application\Controller\MainController',<br>
        'action' => 'notfound'<br>
        ]
    </code>
</p>